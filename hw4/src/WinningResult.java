// This java class stores the references mean and standard deviation references


package swe645_3;


public class WinningResult {
	private float mean;
	private double sd;
	
	public WinningResult(float mean, double sd)
	{
		this.mean = mean;
		this.sd = sd;
	}
	public float getMean() {
		return mean;
	}

	public double getSd() {
		return sd;
	}
}

